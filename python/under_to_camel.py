def zebulansNightmare(functionName):
    """(str) -> str
    Your job is to take PEP8 compatible function
    names and convert them to camelCase.
    >>> zebulansNightmare('camel_case')
    'camelCase'
    """
    
    b = functionName.split('_')
    for item in range(1, len(b)):
        b[item] = b[item].capitalize()
    b = "".join(b)
    return b
